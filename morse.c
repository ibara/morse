#include <stdio.h>

const char *tab[] = {
	"-----",
	".----",
	"..---",
	"...--",
	"....-",
	".....",
	"-....",
	"--...",
	"---..",
	"----.",
	"\0",
	"\0",
	"\0",
	"\0",
	"\0",
	"\0",
	"\0",
	".-",
	"-...",
	"-.-.",
	"-..",
	".",
	"..-.",
	"--.",
	"....",
	"..",
	".---",
	"-.-",
	".-..",
	"--",
	"-.",
	"---",
	".--.",
	"--.-",
	".-.",
	"...",
	"-",
	"..-",
	"...-",
	".--",
	"-..-",
	"-.--",
	"--.."
};

int
main(int argc, char *argv[])
{
	char *argp;
	int c;

	while (*++argv) {
		argp = *argv;
		c = 0;
		while (argp[c] != '\0') {
			fputs(tab[argp[c++] - 0x30], stdout);
			putchar(' ');
		}

		putchar('\n');
	}

	return 0;
}
